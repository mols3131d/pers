# 약어는 변수와 함수에만 적용합니다.
# 함수는 모든 약어가 적용되는 게 아니라 잘 알려진 것들만 적용할게요.

# ==============================================================================================
# <수학>
# pos: position(좌표, 위치) (함수에도 적용)
# calc: calculate(연산) (함수에도 적용)
# num: number(숫자) (함수에도 적용)
# rect: rectangle(사각형) (함수에도 적용)

# ==============================================================================================
# <Win32 API>
# wnd: window(윈도우)
# wc: window class(윈도우 클래스)
# dw: double word(4바이트 부호없는 정수)
# h: handle(핸들)

# ==============================================================================================
# <UI>
# btn: button(버튼)
# txt: text(텍스트)

# ==============================================================================================
# <게임>
# chara: character(캐릭터) (함수에도 적용)
# inven: inventory(인벤토리) (함수에도 적용)

# ==============================================================================================
# <그래픽>
# DX: directX(다이렉트X) (함수에도 적용)
# RGBA: red green blue alpha(픽셀당 색상값) (함수에도 적용)
# img: image(이미지)

# ==============================================================================================
# <나머지>
# lhs: left hand side(왼쪽값)
# rhs: right hand side(오른쪽값)
# sp: smart pointer(스마트 포인터)
# str: string(멀티바이트 문자열) (wstr은 유니코드 문자열)
# sz: string terminate zero(종료문자가 있는 문자열) (wsz는 유니코드 문자열)
# idx: index(인덱스) (함수에도 적용)
# alloc: allocation(할당) (함수에도 적용)
# func: function(함수) (함수에도 적용)
# ret: return(반환)
# elem: element(요소) (함수에도 적용)
# arg: argument(인자) (함수에도 적용)
# util: utility(유틸리티) (함수에도 적용)
# iter: iterator(반복자) (함수에도 적용)
# ref: reference(참조자) (함수에도 적용)
# pred: predication(비교할 때 보조하는 연산) (함수에도 적용)
# src: source(원본) (함수에도 적용)
# dest: destination(목적지) (함수에도 적용)
# app: application(응용 프로그램) (함수에도 적용)
# impl: implementation(구현) (함수에도 적용)
# cmd: command(명령)
# msg: message(메시지)
# doc: document(문서)
# temp: temporariness(임시) (함수에도 적용)
# init: initialize(초기화) (함수에도 적용)
# std: standard(표준) (함수에도 적용)
# info: information(정보) (함수에도 적용)
# mgr: manager(관리자)
# ctx: context(객체가 사용할 정보를 모아놓은 것)
# attr: attribute(속성) (함수에도 적용)
# avg: average(평균)
# arr: array(배열)